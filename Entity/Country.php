<?php

namespace Eotvos\VersenyBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Contains basic information about a country.
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="Eotvos\VersenyBundle\Entity\CountryRepository")
 * 
 * @author    Zsolt Parragi <zsolt.parragi@cancellar.hu> 
 * @copyright 2012 Cancellar
 * @license   MIT License (http://www.opensource.org/licenses/mit-license.php)
 * @version   Release: v0.1
 */
class Country
{
    /**
     * @var integer $id
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string $name
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name Name of the country
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * A country's string representation is it's name.
     * 
     * @return string
     */
    public function __toString()
    {
        return $this->name;
    }
    
}
